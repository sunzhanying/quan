package com.jeesite.API.service;

public class Coupon {
    private String orgCode;
    private String memberId;
    private String code;
    private String categoryId;
    private String categoryCode;
    private String name;
    private String type;
    private String amount;
    private String effectBegin;
    private String effectEnd;
    private String ownerActivity;
    private String state;
    private String fixed;
    private String discount;

    public String getOrgCode() {
        return orgCode;
    }

    public void setOrgCode(String orgCode) {
        this.orgCode = orgCode;
    }

    public String getMemberId() {
        return memberId;
    }

    public void setMemberId(String memberId) {
        this.memberId = memberId;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(String categoryId) {
        this.categoryId = categoryId;
    }

    public String getCategoryCode() {
        return categoryCode;
    }

    public void setCategoryCode(String categoryCode) {
        this.categoryCode = categoryCode;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getEffectBegin() {
        return effectBegin;
    }

    public void setEffectBegin(String effectBegin) {
        this.effectBegin = effectBegin;
    }

    public String getEffectEnd() {
        return effectEnd;
    }

    public void setEffectEnd(String effectEnd) {
        this.effectEnd = effectEnd;
    }

    public String getOwnerActivity() {
        return ownerActivity;
    }

    public void setOwnerActivity(String ownerActivity) {
        this.ownerActivity = ownerActivity;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getFixed() {
        return fixed;
    }

    public void setFixed(String fixed) {
        this.fixed = fixed;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }
}
